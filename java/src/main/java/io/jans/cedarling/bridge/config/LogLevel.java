/**
 * Cedarling Logging Levels
 */
public enum LogLevel {
    /**
     * Fatal logging level 
     */
    FATAL,
    /**
     * Error logging level 
     */
    ERROR,
    /**
     * Warning logging level
     */
    WARN,
    /**
     * Info logging level
     */
    INFO,
    /**
     * Debug logging level
     */
    DEBUG,
    /**
     * Trace logging level 
     */
    TRACE
}
